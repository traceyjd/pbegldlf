
# While loopy loops

# While loops continue looping while a condition is True

x = 1
while x != 3: # like saying do this as long as x does not equal 3
    print('x is', x)
    x = x + 1  # The loop would run forever unless we changed x so the contitional could eventually be false.

# 1st loop x is 1
# 2nd loop x is 2
# 3rd loop loop is done running as it now equals 3, our conditional was set to not equal 3 keep looping