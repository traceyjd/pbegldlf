
# Comparing Lists

# How do we check if two lists are equal????
# They have to have the same items and in exactly the same order

my_list = [1, 2, 3, 4]
your_list = [4, 3, 2, 1]
his_lst = [1, 2, 3, 4]

print(my_list == your_list) # evaluates to false
print(my_list == his_lst) # evaluates to true



